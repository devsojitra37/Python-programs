class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def insert(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
        else:
            current = self.head
            while current.next is not None:
                current = current.next
            current.next = new_node

    def print_list(self):
        current = self.head
        while current is not None:
            print(current.data)
            current = current.next

# create a linked list
ll = LinkedList()

# insert elements
ll.insert(10)
ll.insert(20)
ll.insert(30)
ll.insert(40)

# print the linked list
ll.print_list()